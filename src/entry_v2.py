class DeadDependencyForwarder:
    """
    Forwards unsupported dependencies to workers that CAN support them.
    All dead packages (psutil, torch, tensorflow, qiskit, etc.) go through this.
    """
    
    def __init__(self, worker_id: str):
        self.worker_id = worker_id
        self.capable_workers = {}
        self.pending_requests = {}
        self.installed_modules = set()
        self._lock = threading.Lock()
        self.local_capabilities = []
        self._detect_capabilities()
        self._register_self()
        print(f"🔄 DEAD DEPENDENCY FORWARDER initialized for {worker_id}")
        print(f"   Local capabilities: {self.local_capabilities}")
    
    def _detect_capabilities(self):
        """Detect what this worker can actually run"""
        for module in ['numpy', 'scipy', 'pandas', 'scikit-learn', 'matplotlib']:
            try:
                importlib.import_module(module)
                self.local_capabilities.append(module)
            except:
                pass
    
    def _register_self(self):
        """Register this worker in the holographic DB"""
        try:
            from js import HolographicDB
            db = HolographicDB
            registration = json.dumps({
                'worker_id': self.worker_id,
                'capabilities': self.local_capabilities,
                'timestamp': time.time(),
                'type': 'worker_registration'
            })
            db._raid_write(registration, 'registry_worker')
        except:
            pass
    
    def forward(self, module: str, method: str, *args, **kwargs) -> Any:
        """Forward a dead dependency to a capable worker"""
        
        # Check if we have it locally
        if module in self.local_capabilities:
            try:
                mod = importlib.import_module(module)
                return getattr(mod, method)(*args, **kwargs)
            except:
                pass
        
        # Try to find a capable worker via holographic DB
        try:
            from js import HolographicDB
            db = HolographicDB
            results = db._raid_discover('capability_' + module, ' ' * 4096, 4096)
            if results:
                # Return a forward response
                return {
                    'status': 'forwarded',
                    'module': module,
                    'method': method,
                    'target': results.split(',')[0] if isinstance(results, str) else results[0],
                    'message': f'Forwarded {module}.{method} to capable worker'
                }
        except:
            pass
        
        # Store pending
        request_id = f"req_{uuid.uuid4().hex[:8]}"
        self.pending_requests[request_id] = {
            'module': module,
            'method': method,
            'args': args,
            'kwargs': kwargs,
            'timestamp': time.time()
        }
        
        return {
            'status': 'pending',
            'request_id': request_id,
            'module': module,
            'method': method,
            'message': f'No worker capable of {module}. Request queued.'
        }
    
    def status(self) -> Dict:
        return {
            'local_capabilities': self.local_capabilities,
            'pending_requests': len(self.pending_requests),
            'forwarder_active': True
        }

