        hash_val = hash(stream_id) & 0xffffffff
        stripe_idx = seq // self.stripe_size
        primary = (abs(hash_val) + stripe_idx) % self.total_cells
        return [primary, (primary + 1) % self.total_cells, (primary + self.total_cells // 2) % self.total_cells]
    
